cfg = configparser.ConfigParser()
cfg.read_string(SAMPLE_CONFIG)
keytabs: dict[str, Any] = {}


def TestOneInput(data):
    try:
        state = GMSAState(cfg["sample"], keytabs)
        state.parse_managedpassword_blob(data)
    except ValueError:
        # Expected structure validation exceptions
        pass
    except Exception as e:
        # Unexpected errors (like OverflowError/OSError in datetime conversion)
        raise e


def bootstrap_seed_corpus(dirs):
    if not dirs:
        return
    corpus_dir = Path(dirs[0])
    if corpus_dir.exists() and not any(corpus_dir.iterdir()):
        # Write valid sample password blobs as seeds
        (corpus_dir / "seed_blob1.bin").write_bytes(SAMPLE_BLOB)
        (corpus_dir / "seed_blob2.bin").write_bytes(SAMPLE_BLOB_2)
        print(f"Bootstrapped empty corpus directory '{corpus_dir}' with valid GMSA password blobs.")


def main():
    if atheris is None:
        print("Atheris is not installed. Fuzzing is only supported on environments where Atheris is available (e.g. Linux).")
        sys.exit(0)

    logging.disable(logging.CRITICAL)

    # Filter out atheris flags (start with -) to extract corpus dirs
    corpus_dirs = [arg for arg in sys.argv[1:] if not arg.startswith("-")]
    bootstrap_seed_corpus(corpus_dirs)

    atheris.Setup(sys.argv, TestOneInput)
    atheris.Fuzz()


if __name__ == "__main__":
    main()
